function love.conf(t)
    t.title = "Have fun!"	-- The title of the window the game is in (string)
    t.author = "Mikko Savinainen & Markus Ylinen"		-- The author of the game (string)
    t.screen.width = 1920			-- The window width (number)
    t.screen.height = 1200			-- The window height (number)
    t.screen.fullscreen = false		-- Enable fullscreen (boolean)
    t.screen.vsync = false			-- Enable vertical sync (boolean)
end
